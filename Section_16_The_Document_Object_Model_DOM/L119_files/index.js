// Getting the classList property of the button:
document.querySelector("button").classList;

// Adding a class "invisible" to the button element:
document.querySelector("button").classList.add("invisible");

// Removing the "invisible" class from the button element:
document.querySelector("button").classList.remove("invisible");

// Toggling the "invisible" class on the button element:
document.querySelector("button").classList.toggle("invisible");

// Lesson Challenge - Toggling the huge class on the h1 element:
document.querySelector("h1").classList.toggle("huge");