// Ordinary means of incrementation:
var x = 5;
x = x + 1; // x = 6

// Incrementation in maths:
// shorthard increment (increases x by 1) called Increment Expression:
x++;

// Decrement Expression (reduces a variable by 1):
x--;

// Shorthand for increasing a variable by more than 1:
x += 3; // short for x = x + 3;

// Shorthand for decreasing a variable by more than 1:
x -= 3; // short for x = x - 3;

// Shorthand for multiplying a variable by a value:
x *= 3; // short for x = x * 3;

// Shorthand for dividing a variable by a value:
x /= 3; // short for x = x / 3;