// === vs ==
var a = 1; //a number
var b = "1"; //a string

// Using ===
if (a === b){
    console.log("Yes");
} else {
    console.log("No");
} // This conditional will output No to the console.

// Using ==
if (a == b){
    console.log("Yes");
} else {
    console.log("No");
} // This conditional will output Yes to the console.